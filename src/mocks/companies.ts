export function getCompanyById(id: string): CompanyProfile | undefined {
  return ALL_COMPANIES.find(c => c.id === id);
}

export function getCompaniesByType(type: CompanyType): CompanyProfile[] {
  return ALL_COMPANIES.filter(c => c.company_type === type);
}

export function getCompaniesListItems(): CompanyListItem[] {
  return ALL_COMPANIES.map(c => ({
    id: c.id,
    name: c.name,
    short_name: c.short_name,
    company_type: c.company_type,
    logo_url: c.logo_url,
    city: c.city,
    rating: c.rating,
    reviews_count: c.reviews_count,
    specializations: c.specializations,
    is_verified: c.is_verified
  }));
}

